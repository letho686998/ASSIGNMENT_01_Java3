    private void loadtable() {
        DefaultTableModel model1 = (DefaultTableModel) tblsanpham.getModel();
        model1.setColumnCount(0);
        model1.addColumn("productId");
        model1.addColumn("ProductName");
        model1.addColumn("Tổng số lượng");
        model1.setRowCount(0);
        ArrayList<Baocao> danhsachtop10 = service.top10SpBanchay();
        for (Baocao xn : danhsachtop10) {
            model1.addRow(new Object[]{xn.getIdsp(), xn.getTensp(), xn.getSoluong()});
        }

    }

